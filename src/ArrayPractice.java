
public class ArrayPractice {

	public static void main(String[] args) {
		String name[] = {"Ram","Sham","Raju","Sita","Gita"};
		int  len = name.length;
		System.out.println(len);// prints total no. of items
		System.out.println(name[4]);    //prints according to index no.
		
		
		for (int i=0; i<name.length; i++)
		{
			System.out.println(name[i]); //prints all items next line
}				
				
	} 
		
}
